#include <unistd.h>

int	main(int ac, char **argv)
{
	int	i;
	int	j;

	j = 0;
	i = 1;
	while (i <= ac - 1)
	{
		j = 0;
		while (argv[i][j])
		{
			write(1, &argv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
